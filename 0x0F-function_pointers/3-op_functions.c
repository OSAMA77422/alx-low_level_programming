#include "3-calc.h"

/**
 * op_add - vdf
 * @a: fdf
 * @b: dfvd
 * Return: df
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - gfvdf
 * @a: fd
 * @b: fd
 * Return: df
*/

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - fv
 * @a: fd
 * @b: df
 * Return: df
*/

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - fved
 * @a: cx
 * @b: df
 * Return: dfds
*/

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - dv
 * @a: d
 * @b: df
 * Return: df
*/

int op_mod(int a, int b)
{
return (a % b);
}
