/**
 * *_strstr - i
 *
 * @haystack: vfd
 * @needle: fevdf
 *
 * Return: vdf
*/

char *_strstr(char *haystack, char *needle)
{
int i, j = 0;

for (i = 0; haystack[i] = needle[i]; i++)
{
j++;
}
return (j);
for (i = j; haystack[i] != '\0'; i++)
{
return (haystack);
}
return ('\0');
}
